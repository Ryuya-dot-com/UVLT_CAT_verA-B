import { chmod, link, mkdir, readFile, unlink, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";
import {
  RANDOMIZATION_ALGORITHM,
  OPTION_LAYOUT_ALGORITHM,
  buildBalanceAudit,
  generateAllocationSchedule,
  payloadSha256,
  sha256Hex,
  stableStringify,
  validateRecruitmentPolicy,
  validateWilliamsRouteBalance
} from "./randomization-design.mjs";

const here = path.dirname(fileURLToPath(import.meta.url));
const project = path.resolve(here, "../..");
const privateDirectory = path.join(project, "cloudflare", "private");

function assert(condition, message) {
  if (!condition) throw new Error(message);
}

function isPlainObject(value) {
  return Boolean(value) && typeof value === "object" && !Array.isArray(value) &&
    Object.getPrototypeOf(value) === Object.prototype;
}

function parseArguments(argv) {
  const supported = new Set(["--config", "--routes", "--schedule-output", "--audit-output"]);
  const parsed = new Map();
  for (let index = 0; index < argv.length; index += 1) {
    const key = argv[index];
    assert(supported.has(key),
      "Arguments must be --config, --routes, --schedule-output, and/or --audit-output paths");
    assert(!parsed.has(key), `${key} may be supplied only once`);
    const value = argv[index + 1];
    assert(value !== undefined && !value.startsWith("--"), `${key} requires a path`);
    parsed.set(key, value);
    index += 1;
  }
  return parsed;
}

function resolveProjectPath(value, fallback) {
  return path.resolve(project, value || fallback);
}

function assertPrivateOutput(outputPath, label) {
  const relative = path.relative(privateDirectory, outputPath);
  assert(relative && !relative.startsWith("..") && !path.isAbsolute(relative),
    `${label} must be a file inside cloudflare/private`);
}

async function readJson(file, label) {
  const bytes = await readFile(file);
  let text;
  try {
    text = new TextDecoder("utf-8", { fatal: true }).decode(bytes);
  } catch {
    throw new Error(`${label} must be valid UTF-8`);
  }
  try {
    return JSON.parse(text);
  } catch {
    throw new Error(`${label} must be valid JSON`);
  }
}

async function atomicPrivateWrite(outputPath, bytes) {
  const encoded = Buffer.from(bytes, "utf8");
  await mkdir(path.dirname(outputPath), { recursive: true, mode: 0o700 });
  await chmod(privateDirectory, 0o700);
  await chmod(path.dirname(outputPath), 0o700);
  try {
    const existing = await readFile(outputPath);
    assert(existing.equals(encoded),
      `${path.relative(project, outputPath)} is frozen and differs from the generated bytes`);
    await chmod(outputPath, 0o600);
    return "unchanged";
  } catch (error) {
    if (error?.code !== "ENOENT") throw error;
  }
  const temporaryPath = path.join(
    path.dirname(outputPath),
    `.${path.basename(outputPath)}.tmp-${process.pid}-${Date.now()}`
  );
  try {
    await writeFile(temporaryPath, encoded, { flag: "wx", mode: 0o600 });
    try {
      // A hard-link publishes the complete temporary file without an
      // overwrite window. EEXIST is handled as a concurrent/frozen write.
      await link(temporaryPath, outputPath);
    } catch (error) {
      if (error?.code !== "EEXIST") throw error;
      const existing = await readFile(outputPath);
      assert(existing.equals(encoded),
        `${path.relative(project, outputPath)} was concurrently frozen with different bytes`);
      await chmod(outputPath, 0o600);
      return "unchanged";
    }
    await chmod(outputPath, 0o600);
    return "created";
  } catch (error) {
    throw error;
  } finally {
    await unlink(temporaryPath).catch(() => {});
  }
}

const args = parseArguments(process.argv.slice(2));
const configPath = resolveProjectPath(args.get("--config"), "cloudflare/private/release-config.json");
const routesPath = resolveProjectPath(args.get("--routes"), "data/uvlt_routes.ab.williams10.dev.json");
const scheduleOutputPath = resolveProjectPath(
  args.get("--schedule-output"),
  "cloudflare/private/randomization-schedule.json"
);
const auditOutputPath = resolveProjectPath(
  args.get("--audit-output"),
  "cloudflare/private/design-balance.audit.json"
);
assertPrivateOutput(scheduleOutputPath, "Schedule output");
assertPrivateOutput(auditOutputPath, "Balance audit output");
assert(scheduleOutputPath !== auditOutputPath, "Schedule and balance audit outputs must be different files");

const seed = process.env.UVLT_RANDOMIZATION_SEED;
assert(typeof seed === "string", "UVLT_RANDOMIZATION_SEED must be set in the process environment");

const [config, routes] = await Promise.all([
  readJson(configPath, "Release config"),
  readJson(routesPath, "Route artifact")
]);
assert(isPlainObject(config), "Release config must be a plain object");
assert(config.schemaVersion === "uvlt-fixed-ab-field-release-config-6",
  "Randomization schedule generation requires release config schema v6");
validateRecruitmentPolicy(config.recruitmentPolicy);
assert(typeof config.releaseId === "string", "Release config must contain releaseId");
assert(isPlainObject(config.expectedHashes), "Release config must contain expectedHashes");
assert(typeof config.expectedHashes.routesPayloadSha256 === "string" &&
  /^[0-9a-f]{64}$/u.test(config.expectedHashes.routesPayloadSha256),
"Release config expectedHashes.routesPayloadSha256 must be a lowercase SHA-256 value");
if (config.randomizationAlgorithm !== undefined && config.randomizationAlgorithm !== null) {
  assert(config.randomizationAlgorithm === RANDOMIZATION_ALGORITHM,
    "Release config randomizationAlgorithm does not match the implemented algorithm");
}
if (config.optionLayoutAlgorithm !== undefined && config.optionLayoutAlgorithm !== null) {
  assert(config.optionLayoutAlgorithm === OPTION_LAYOUT_ALGORITHM,
    "Release config optionLayoutAlgorithm does not match the implemented algorithm");
}
if (config.randomizationSeedFingerprint !== undefined &&
    config.randomizationSeedFingerprint !== null) {
  assert(/^sha256:[0-9a-f]{64}$/u.test(config.randomizationSeedFingerprint),
    "Release config randomizationSeedFingerprint must be sha256:<64 lowercase hex>");
}

validateWilliamsRouteBalance(routes);
assert(isPlainObject(routes.integrity), "Route artifact must contain integrity metadata");
assert(typeof routes.integrity.payloadSha256 === "string" &&
  /^[0-9a-f]{64}$/u.test(routes.integrity.payloadSha256),
"Route artifact integrity.payloadSha256 must be a lowercase SHA-256 value");
assert(payloadSha256(routes) === routes.integrity.payloadSha256,
  "Route artifact payload hash is invalid");
assert(routes.integrity.payloadSha256 === config.expectedHashes.routesPayloadSha256,
  "Release config does not pin the supplied route artifact");

const schedule = generateAllocationSchedule({
  seed,
  releaseId: config.releaseId,
  routesPayloadSha256: routes.integrity.payloadSha256
});
const configuredScheduleHash = config.expectedHashes.allocationScheduleSha256;
if (configuredScheduleHash !== undefined && configuredScheduleHash !== null &&
    configuredScheduleHash !== "0".repeat(64)) {
  assert(configuredScheduleHash === schedule.integrity.payloadSha256,
    "Release config does not pin the generated allocation schedule");
}
if (config.randomizationSeedFingerprint !== undefined &&
    config.randomizationSeedFingerprint !== null) {
  assert(config.randomizationSeedFingerprint === schedule.seedFingerprint,
    "Release config does not pin the supplied randomization seed");
}
if (config.active === true) {
  assert(config.randomizationAlgorithm === RANDOMIZATION_ALGORITHM,
    "An active release must pin the randomization algorithm");
  assert(config.optionLayoutAlgorithm === OPTION_LAYOUT_ALGORITHM,
    "An active release must pin the option layout algorithm");
  assert(config.randomizationSeedFingerprint === schedule.seedFingerprint,
    "An active release must pin the randomization seed fingerprint");
  assert(configuredScheduleHash === schedule.integrity.payloadSha256,
    "An active release must pin the generated allocation schedule");
}

const audit = buildBalanceAudit({ schedule, routes });
const scheduleBytes = `${stableStringify(schedule)}\n`;
const auditBytes = `${stableStringify(audit)}\n`;
const scheduleWrite = await atomicPrivateWrite(scheduleOutputPath, scheduleBytes);
const auditWrite = await atomicPrivateWrite(auditOutputPath, auditBytes);

process.stdout.write(`${JSON.stringify({
  ok: true,
  scheduleOutput: path.relative(project, scheduleOutputPath),
  balanceAuditOutput: path.relative(project, auditOutputPath),
  scheduleWrite,
  balanceAuditWrite: auditWrite,
  allocationScheduleSha256: schedule.integrity.payloadSha256,
  allocationScheduleFileSha256: sha256Hex(scheduleBytes),
  balanceAuditSha256: audit.integrity.payloadSha256,
  balanceAuditFileSha256: sha256Hex(auditBytes),
  seedFingerprint: schedule.seedFingerprint,
  randomizationAlgorithm: RANDOMIZATION_ALGORITHM,
  optionLayoutAlgorithm: OPTION_LAYOUT_ALGORITHM
})}\n`);
